    private void JTListaEstudianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTListaEstudianteMouseClicked
    
        String[] actividades;
        //obtengo la fila seleccionada del JTListaEstudiante
        int filaSeleccionada=JTListaEstudiante.getSelectedRow();
        if(filaSeleccionada>=0){
            
            for (int i = 0; i < modeloActividad.getRowCount(); i++) {
                    modeloActividad.removeRow(i);
            }
            
            
            String matricula=JTListaEstudiante.getValueAt(filaSeleccionada, 0).toString();

            for (int i = 0; i <listaEstudiante.size(); i++) {
                 if(matricula.equals(listaEstudiante.get(i).getMatricula())){
                    listaActividad=f.buscarListaActividadXAlumno(matricula);
                     for (int j = 0; j <listaActividad.size(); j++) {
                         actividades=new String[4];
                         actividades[0]=listaActividad.get(j).getNombre();
                         actividades[1]=listaActividad.get(j).getDescripcion();
                         actividades[2]=listaActividad.get(j).getFecha_entrega();
                         actividades[3]=listaActividad.get(j).getCalificacion();
                         modeloActividad.addRow(actividades);
                     }
                 }
            }
        }
        
        System.out.println("Selecionaste una fila");
    }//GEN-LAST:event_JTListaEstudianteMouseClicked

   
