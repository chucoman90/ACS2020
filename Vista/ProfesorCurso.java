        for (int i = 0; i <listaMateria.size(); i++) {
            if(materiaSeleccionada.equals(listaMateria.get(i).getNombre())){
                listaEstudiante=f.buscarListaAlumnoXMateria(listaMateria.get(i).getId());
                for (int j = 0; j < listaEstudiante.size(); j++) {
                     alumnos=new String[4];
                     alumnos[0]=listaEstudiante.get(j).getMatricula();
                     alumnos[1]=listaEstudiante.get(j).getNombre();
                     alumnos[2]=listaEstudiante.get(j).getAp_paterno();
                     alumnos[3]=listaEstudiante.get(j).getAp_materno();
                     modeloEstudiante.addRow(alumnos);
                }
                
            }
        }
        
        
       

        
        
